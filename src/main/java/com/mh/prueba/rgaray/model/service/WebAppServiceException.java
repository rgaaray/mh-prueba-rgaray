package com.mh.prueba.rgaray.model.service;

public class WebAppServiceException extends Exception {

	/**
	 * Serial UID.
	 */
	private static final long serialVersionUID = 1133913848087025844L;

	public WebAppServiceException() {
		super();
	}

	public WebAppServiceException(String message, Throwable cause) {
		super(message, cause);
	}

	public WebAppServiceException(String message) {
		super(message);
	}

	public WebAppServiceException(Throwable cause) {
		super(cause);
	}

}
